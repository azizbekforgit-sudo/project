const I18nManager = {
  current: localStorage.getItem('av_lang') || 'uz', // по умолчанию узбекский

  get(key) {
    const dict = I18N[this.current] || I18N.uz;
    return dict[key] || I18N.uz[key] || key;
  },

  set(lang) {
    if (!I18N[lang]) return;
    this.current = lang;
    localStorage.setItem('av_lang', lang);
    // перерисовать текущую страницу
    if (window.router && typeof window.router.reload === 'function') {
      window.router.reload();
    } else {
      location.reload();
    }
  },

  langs() {
    return [
      { code: 'uz', label: "O‘zbekcha", flag: '🇺🇿' },
      { code: 'ru', label: 'Русский',   flag: '🇷🇺' },
      { code: 'en', label: 'English',   flag: '🇬🇧' },
    ];
  },
};

// короткий хелпер
function t(key) { return I18nManager.get(key); }

window.I18nManager = I18nManager;
window.t = t;
